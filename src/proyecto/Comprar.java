    private void aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarActionPerformed
        
        if (Double.parseDouble(cantidadComprar.getText()) <= Double.parseDouble(comprarCant.getText())) {
            boolean aceptarProducto = false;
            double cantidad_a_comprar = 0;
            double cantidad_disponible;
        
            if (cantidadComprar.getText().equals(null)) {
                aceptarProducto = false;
            }else{
                aceptarProducto = true;
            }
        
            if (aceptarProducto == true) {
                double precio_pagar;
                
                String tipoProdu1 = comprarTipo.getText();
                String codigoP1 = comprarCodigo.getText();
                String nombre1 = comprarNombre.getText().toLowerCase();
                String unidad1 = comprarUnidad.getText();
                cantidad_a_comprar = Double.parseDouble(cantidadComprar.getText());
                double costo = Double.parseDouble(comprarPrecio.getText());
            
                precio_pagar = cantidad_a_comprar * costo;
                pagar = pagar + precio_pagar;
            
                // creamos el objeto factura
                Factura factura = new Factura(tipoProdu1, codigoP1, nombre1, unidad1, cantidad_a_comprar, precio_pagar);
                // agregamos producto a nuestra lista factura
                facturalista.add(factura);
            
                visualizar_productosVenta_en_la_lista();
                this.setVisible(false);
            }else{
                JOptionPane.showMessageDialog(null, "Ingrese cantidad");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Cantidad no disponible");
            limpiarCantidad();
        }
        
    }//GEN-LAST:event_aceptarActionPerformed

