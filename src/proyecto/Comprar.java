    public static void visualizar_productosVenta_en_la_lista(){
            // recorrer los productos de la lista de productos
            String[] prodCompra = new String[facturalista.size()]; // arreglo
            int cont = 0;
            
            for (Factura comprasP : facturalista) {
                prodCompra[cont] = comprasP.getNombre1() +" "+comprasP.getTipoProducto1()+" "+comprasP.getCantidad1()+" "+comprasP.getMedida1()+" "+comprasP.getPrecio1();
                cont++;
            }
            
            Venta.visualizarVenta.setListData(prodCompra);
        }
    
