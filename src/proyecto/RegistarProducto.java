    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        // 
        String tipoProdu = tipoPr.getText();
        String codigoP = codigoProducto.getText();
        String nombre = nombreProducto.getText().toLowerCase();
        String unidad = unidadM.getText();
        double cantidad = Double.parseDouble(cantidadProducto.getText());
        double precio = Double.parseDouble(precioProducto.getText());
        
        // creamos el objeto producto
        Producto producto = new Producto(tipoProdu, codigoP, nombre, unidad, cantidad, precio);
        // agregamos el producto a nuestra lista
        productos.add(producto);
        
        limpiar();
        visualizar_productos_en_la_lista();
    }//GEN-LAST:event_guardarActionPerformed

        public static void visualizar_productos_en_la_lista(){
            // recorrer los productos de la lista de productos
            String[] prodNombres = new String[productos.size()]; // arreglo
            int cont = 0;
            
            for (Producto producto : productos) {
                prodNombres[cont] = producto.getNombre() +"     "+producto.getTipoProducto();
                cont++;
            }
            
            visualizarRegistros.setListData(prodNombres);
        }
        
        public void limpiar(){
            codigoProducto.setText("");
            nombreProducto.setText("");
            unidadM.setText("");
            cantidadProducto.setText("");
            precioProducto.setText("");
            
        }
        
        
